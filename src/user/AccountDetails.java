    // Add method to update image path in database
    private void updateUserImagePath(int userId, String imagePath) {
        try {
            dbConnector dbc = new dbConnector();
            String updateQuery = "UPDATE tbl_user SET image = '" + imagePath + "' WHERE u_id = '" + userId + "'";
            dbc.updateData(updateQuery);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error updating image path in database: " + ex.getMessage());
        }
    }

    // Utility method to set profile image with fallback (now uses userprofile label)
    private void setProfileImage(String imageFileName) {
        String basePath = "src/images/";
        String imagePath = basePath + (imageFileName != null ? imageFileName : "");
        java.io.File imgFile = new java.io.File(imagePath);
        config.profile prof = new config.profile();
        if (imgFile.exists() && imgFile.isFile()) {
            userprofile.setIcon(prof.ResizeImage(imagePath, null, userprofile));
        } else {
            userprofile.setIcon(prof.ResizeImage(basePath + "defaultimage.png", null, userprofile));
        }
    }

