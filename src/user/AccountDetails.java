    private void saveInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveInfoActionPerformed
        try {
            String newFname = firstnamef.getText().trim();
            String newLname = lastnamef.getText().trim();
            String newEmail = emailf.getText().trim();
            Session sess = Session.getInstance();

            if (newFname.isEmpty() || newLname.isEmpty() || newEmail.isEmpty()) {
                JOptionPane.showMessageDialog(null, "First name, last name, and email cannot be empty.");
                return;
            }

            dbConnector dbc = new dbConnector();
            String updateQuery = "UPDATE tbl_user SET u_firstname = '" + newFname + "', u_lastname = '" + newLname + "', u_email = '" + newEmail + "', image = '" + sess.getImage() + "' WHERE u_id = '" + sess.getUid() + "'";
            dbc.updateData(updateQuery);

            // Update session data
            sess.setFname(newFname);
            sess.setLname(newLname);
            sess.setEmail(newEmail);

            JOptionPane.showMessageDialog(null, "Information updated successfully.");

            // Refresh displayed data
            firstnamef.setText(newFname);
            lastnamef.setText(newLname);
            emailf.setText(newEmail);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error updating information: " + ex.getMessage());
        }
    }//GEN-LAST:event_saveInfoActionPerformed

