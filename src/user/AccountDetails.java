    private void selectimageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectimageActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Image files", "jpg", "jpeg", "png", "gif"));
        
        int returnValue = fileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            try {
                File selectedFile = fileChooser.getSelectedFile();
                if (!selectedFile.exists() || !selectedFile.isFile()) {
                    JOptionPane.showMessageDialog(this, "Selected file does not exist.");
                    return;
                }

                // Create images directory if it doesn't exist
                String imagesDirPath = "src/images";
                File imagesDir = new File(imagesDirPath);
                if (!imagesDir.exists()) {
                    boolean created = imagesDir.mkdirs();
                    if (!created) {
                        JOptionPane.showMessageDialog(this, "Failed to create images directory.");
                        return;
                    }
                }

                // Generate unique filename to avoid conflicts
                String fileName = System.currentTimeMillis() + "_" + selectedFile.getName().replaceAll("[\\s]+", "_");
                String destination = imagesDirPath + File.separator + fileName;
                
                // Copy the file first
                try {
                    config.profile prof = new config.profile();
                    prof.imageUpdater(destination, selectedFile.getAbsolutePath());
                    
                    // Set the image icon only after successful copy
                    userprofile.setIcon(prof.ResizeImage(destination, null, userprofile));

                    // Update user's image path in database and session
                config.Session session = config.Session.getInstance();
                    
                // Update image path in database (store only filename)
                updateUserImagePath(session.getUid(), fileName);
                    
                // Update session
                session.setImage(fileName);

                    JOptionPane.showMessageDialog(this, "Profile picture updated successfully!");
                } catch (Exception copyEx) {
                    JOptionPane.showMessageDialog(this, "Error copying image file: " + copyEx.getMessage());
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error updating profile picture: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_selectimageActionPerformed

